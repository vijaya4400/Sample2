package datatypes;

public class Narrowing {
	public static void main(String[] args)
	{
		 double x=178.7997;
		long y=(long)x;
		float z=(float)x;
		int k=(int)x;
		System.out.println("x:"+x);
		System.out.println("y:"+y);
		System.out.println("z:"+z);
		System.out.println("k:"+k);
	}

}
